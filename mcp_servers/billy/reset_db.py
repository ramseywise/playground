    print(f"  Inserted {len(INVOICE_LINES)} invoice lines")

    conn.executemany("INSERT INTO counters (name,value) VALUES (?,?)", COUNTERS)

    conn.execute("PRAGMA foreign_keys=ON")
    conn.commit()
    print("Done.")

